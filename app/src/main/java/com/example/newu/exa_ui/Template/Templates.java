public class Templates {
    int template;
    String title;

    public Templates(int template, String title) {
        this.template = template;
        this.title = title;
    }


    public int getTemplate() {
        return template;
    }

    public void setTemplate(int template) {
        this.template = template;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
